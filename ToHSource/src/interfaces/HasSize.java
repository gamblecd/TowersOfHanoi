package interfaces;

/**
 * Forces an object to have a size method, to represent the size of the object
 * by an int.
 * 
 * @author $hadow$torm
 * 
 */
public interface HasSize {

	/**
	 * Returns the size of the implementing object in a way that makes sense for
	 * that object.
	 * 
	 * @return int representing size.
	 */
	public int size();
}
